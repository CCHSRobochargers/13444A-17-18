task usercontrol()
{

	//PID fixup
	nMotorPIDSpeedCtrl[rightMotor] = RegIdle;
	nMotorPIDSpeedCtrl[leftMotor] = RegIdle;
	nMotorPIDSpeedCtrl[clawMotor] = RegIdle;
	nMotorPIDSpeedCtrl[rackRotater] = RegIdle;

	while (true)
	{

		leftSpeed = (vexRT[Ch3] * 3) / 4;
		rightSpeed = (vexRT[Ch2] * 3) / 4;

		// Fork D-Pad
		if (vexRT[Btn8U]) {
			leftSpeed = -80;
			rightSpeed = -80;
			} else if (vexRT[Btn8R]) {
			leftSpeed = 35;
			rightSpeed = -35;
			} else if (vexRT[Btn8D]) {
			leftSpeed = 80;
			rightSpeed = 80;
			} else if (vexRT[Btn8L]) {
			leftSpeed = -35;
			rightSpeed = 35;
		}

		// Claw D-Pad
		if (vexRT[Btn7U]) {
			leftSpeed = 80;
			rightSpeed = 80;
			} else if (vexRT[Btn7R]) {
			leftSpeed = -35;
			rightSpeed = 35;
			} else if (vexRT[Btn7D]) {
			leftSpeed = -80;
			rightSpeed = -80;
			} else if (vexRT[Btn7L]) {
			leftSpeed = 35;
			rightSpeed = -35;
		}

		motor[leftMotor] = leftSpeed;
		motor[rightMotor] = rightSpeed;

		if (fabs(vexRT[Ch2Xmtr2]) < 10) {
			motor[rackRotater] = -15;
			} else {
			motor[rackRotater] = -(vexRT[Ch2Xmtr2] / 2);
		}

		motor[forkMotor1] = -vexRT[Ch3Xmtr2];



		if (vexRT[Btn7UXmtr2]) {
			motor[clawMotor] = 50;
			pinch = false;
			} else if (vexRT[Btn7DXmtr2]) {
			motor[clawMotor] = -50;
			pinch = true;
			} else if (pinch) {
			motor[clawMotor] = -12;
			} else if (!pinch) {
			motor[clawMotor] = 0;
		}

	}
}
